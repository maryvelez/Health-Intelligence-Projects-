public class operators {
    public static void main(String[] args) {
        int number1 = 12;
        int number2 = 6;

        //addition
        System.out.println(number1 + number2);

        // Subtraction
        System.out.println(number1 - number2);

        //multiplication
        System.out.println(number1 *number2);

        //division
        System.out.println(number1/number2);

        //remainder (modulo/modulus)
        System.out.println(number1 % number2);
        

    }
}
